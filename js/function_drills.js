function isOdd(number){
    return number % 2 === 1;
}

function isEven(number){
    return number % 2 === 0;
}

function identity(input){
    return input === input;
}

function isFive(input){
    return input + 5;
}

function isMultipleOfFive(input){
    return input * 5;
}

function isThree(input){
    return input === 3;
}

function isMultipleOfThree(input){
    return input * 3;
}